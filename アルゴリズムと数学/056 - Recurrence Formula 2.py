mod = int(1e9+7)

def multiply(A,B):
    C = [[],[],[]]
    C[0].append((A[0][0]*B[0][0]+A[0][1]*B[1][0]+A[0][2]*B[2][0])%mod)
    C[0].append((A[0][0]*B[0][1]+A[0][1]*B[1][1]+A[0][2]*B[2][1])%mod)
    C[0].append((A[0][0]*B[0][2]+A[0][1]*B[1][2]+A[0][2]*B[2][2])%mod)
    C[1].append((A[1][0]*B[0][0]+A[1][1]*B[1][0]+A[1][2]*B[2][0])%mod)
    C[1].append((A[1][0]*B[0][1]+A[1][1]*B[1][1]+A[1][2]*B[2][1])%mod)
    C[1].append((A[1][0]*B[0][2]+A[1][1]*B[1][2]+A[1][2]*B[2][2])%mod)
    C[2].append((A[2][0]*B[0][0]+A[2][1]*B[1][0]+A[2][2]*B[2][0])%mod)
    C[2].append((A[2][0]*B[0][1]+A[2][1]*B[1][1]+A[2][2]*B[2][1])%mod)
    C[2].append((A[2][0]*B[0][2]+A[2][1]*B[1][2]+A[2][2]*B[2][2])%mod)

    return C

N = int(input())
A = [[1,1,1],[1,0,0],[0,1,0]]
ans = [[1,0,0],[0,1,0],[0,0,1]]
for i in range(60):
    if (N-3)&(1<<i):
        ans = multiply(A,ans)
    A = multiply(A,A)

print((ans[0][0]*2+ans[0][1]+ans[0][2])%mod)